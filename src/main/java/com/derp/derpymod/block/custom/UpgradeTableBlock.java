import net.minecraftforge.network.NetworkHooks;
import org.jetbrains.annotations.Nullable;

public class UpgradeTableBlock extends BaseEntityBlock {

    public static final VoxelShape SHAPE = UpgradeTableBlock.box(0, 0, 0, 16, 11, 16);

    public UpgradeTableBlock(Properties properties) {
        super(properties);
    }

    @Override
    public VoxelShape getShape(BlockState p_60555_, BlockGetter p_60556_, BlockPos p_60557_, CollisionContext p_60558_) {
        return SHAPE;
    }

    @Override
    public RenderShape getRenderShape(BlockState p_49232_) {
        return RenderShape.MODEL;
    }

    @Override
    public void onRemove(BlockState state, Level level, BlockPos pos, BlockState newState, boolean isMoving) {
        if (state.getBlock() != newState.getBlock()) {
            BlockEntity blockEntity = level.getBlockEntity(pos);
            if (blockEntity instanceof UpgradeTableBlockEntity) {
                ((UpgradeTableBlockEntity) blockEntity).drops();
            }
        }
        super.onRemove(state, level, pos, newState, isMoving);
    }

    @Nullable
    @Override
    public BlockEntity newBlockEntity(BlockPos blockPos, BlockState blockState) {

        return new UpgradeTableBlockEntity(blockPos, blockState);
    }
}
